#include "3-calc.h"

/**
 * op_add - add int
 * @a: num 1
 * @b: num 2
 *
 * Return: result of op
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtract int
 * @a: num 1
 * @b: num 2
 *
 * Return: result of op
 */

int op_sub(int a, int b)
{
	return (a - b);
}


/**
 * op_mul - multiplies int
 * @a: num 1
 * @b: num 2
 *
 * Return: result of op
 */
int op_mul(int a, int b)
{
	return (a * b);
}


/**
 * op_div - divides int
 * @a: num 1
 * @b: num 2
 *
 * Return: result of op
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - checks for modulous b against a
 * @a: num 1
 * @b: num 2
 *
 * Return: result of op
 */
int op_mod(int a, int b)
{
	return (a % b);
}
